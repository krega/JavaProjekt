public class Liczniki {
    private String Key;
    private String Value;
    public Liczniki(String pierwszy, String drugi)
    {
        Key=pierwszy;
        Value=drugi;
    }

    public String getKey() {
        return Key;
    }

    public void setKey(String Key) {
        this.Key = Key;
    }

    public String getValue() {
        return Value;
    }

    public void setValue(String Value) {
        this.Value = Value;
    }
    
}
